public class Offer {
    private String id;
    private String name;
    private String url;
    private String network;
    private String payout;
    private String status;
    private String geo;
    private String groupOf;
    private String autoPayout;
    private String cap;
    private String upsell;
    private String affNet;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getNetwork() {
        return network;
    }

    public void setNetwork(String network) {
        this.network = network;
    }

    public String getPayout() {
        return payout;
    }

    public void setPayout(String payout) {
        this.payout = payout;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getGeo() {
        return geo;
    }

    public void setGeo(String geo) {
        this.geo = geo;
    }

    public String getGroupOf() {
        return groupOf;
    }

    public void setGroupOf(String groupOf) {
        this.groupOf = groupOf;
    }

    public String getAutoPayout() {
        return autoPayout;
    }

    public void setAutoPayout(String autoPayout) {
        this.autoPayout = autoPayout;
    }

    public String getCap() {
        return cap;
    }

    public void setCap(String cap) {
        this.cap = cap;
    }

    public String getUpsell() {
        return upsell;
    }

    public void setUpsell(String upsell) {
        this.upsell = upsell;
    }

    public String getAffNet() {
        return affNet;
    }

    public void setAffNet(String affNet) {
        this.affNet = affNet;
    }
}
